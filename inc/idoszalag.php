      <div class="item-content">
       <div class="item-title">Az USAID Civil Fenntarthatósági Index 2012 bemutatója</div>
        <div class="item-body">
          <p>Szeptember 16-án némileg rendhagyó ülést tartott a civil szektor jogszabályi
          környezetével foglalkozó informális egyeztető fórum, a Civil Lobbi Kerekasztal.
          A kibővített találkozón került sor az Egyesült Államok Nemzetközi Fejlesztési Szervezete, az USAID
          által immár másfél évtizede minden évben kiadott Civil Fenntarthatósági Index 2012-es magyarországi
          jelentésének bemutatójára is.</p>
          <p>Az index Közép-Európa és Eurázsia 29 országában méri az általános környezet „civilbarátságát” egységes módszertannal,
          7 dimenzió szerint: a jogi környezet, a szervezeti kapacitás, a pénzügyi életképesség, az érdekérvényesítés, a szolgáltatásnyújtás,
          az infrastruktúra és a társadalmi elismertség. A 2012-es magyarországi jelentés, amely egy szakértői panel bevonásával az Ökotárs
          gondozásában készült el, sajnos nem fest rózsás képet a hazai szektor helyzetéről: gyakorlatilag minden mutató értéke
          romlott a korábbi évekhez képest. Így a 7-fokú skála (amelyen az 1-es a legjobb, a 7-es a leggyengébb érték) összesített átlaga
          3 pont lett, ami az index nemzetközi rendszerében az éppen „megerősödött” és az „erősödő” fenntarthatóság kategóriáinak határán áll.
          </p>
         
