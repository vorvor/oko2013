<h2>Our goals</h2> 
<ul> 
    <li>to assist in the realisation of efficient projects of the Hungarian environmental movement with a special focus on projects solving local environmental problems;</li> 
    <li>to enhance the environmental consciousness in Hungary (undertaking environmental education and promotional tasks);</li> 
    <li>to improve the contacts co-operation between the environmental movement and other civil initiatives, to organise and assist in the creation of joint action;</li> 
    <li>to organise intensified international networking, to assist environmental civil initiatives in the neighbouring countries, including those of the Hungarians living beyond the borders of Hungary;</li> 
    <li>to initiate, organise and carry out environmental actions and campaigns;</li> 
    <li>to contribute to the improvement of the standards of environmental civilisation in Hungary, to the strengthening of ecological attitudes, to the dissemination of sustainable life-style models, worldviews, individual and communal life models as well as to enhance their effects.</li> 
</ul> 
