       <div class="item-title">Presenting the USAID 2012 CSO Sustainability Index</div>
        <div class="item-body">
          <p>On the 16th of September the Civil Advocacy Roundtable - an informal forum of discussing the legal environment of the NGO sector - held a somewhat irregular meeting. On the occasion, the Hungarian chapter of the CSO Sustainability Index 2012 published by the USAID annually during the last 15 years was presented as well. </p>
          <p>The index measures the state and sustainability of the civil sector with a unified method across 29 countries of Middle-Europe and Eurasia. The survey measures 7 dimensions: legal environment, organizational capacity, financial viability, advocacy, service provision, infrastructure, and public image.</p>
          <p>The Hungary index that was prepared with the involvement of Ökotárs paints a sad picture of the siutation: compared to previous years virtually all indicators have deteriorated. Thus, the overall average on the 7-point scale (where 1 is the best and 7 is worst) become 3. In the international system of the index it stands on the border of the categories enhanced and evolving sustainability.</p>
         
