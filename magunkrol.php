<h2>Céljainkból</h2> 
<ul> 
<li>a hazai környezetvédő mozgalom, különösen a helyi környezeti problémákat megoldani képes programok megvalósításának támogatása;</li> 
<li>a környezetvédő mozgalmak és civil kezdeményezések közötti kapcsolat erősítése, az együttes cselekvés feltételeinek megszervezése és támogatása;</li> 
<li>részvétel az országos környezeti érdekegyeztetésben és a jogszabályalkotással kapcsolatos szakmai és társadalmi kontroll gyakorlásában;</li> 
<li>hozzájárulás a hazai környezeti kultúra fejlesztéséhez, az ökológiai szemlélet erősödéséhez, a fenntartható életmódmodellek, életfilozófiák, egyéni és közösségi életstílus-minták ismertté válásához és hatásának növeléséhez.</li> 
<li>környezetvédelmi célú akciók és kampányok kezdeményezése, szervezése, lebonyolítása;</li> 
</ul> 
