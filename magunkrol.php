    <h1>Magunkról</h1>
<p>Az Ökotárs Alapítvány nem nyereségérdekelt, politikától független, a környezet állapotának javulásáért, a hazai civil szféra és lakosság környezeti érzékenységének és tudatosságának fejlesztéséért tevékenykedő szervezet.</p> 
<p>Kérjük, amennyiben egyetért céljainkkal és módjában áll, támogassa alapítványunkat, ezen keresztül a környezetvédelem ügyét.</p> 
